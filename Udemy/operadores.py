"""
##########################
# Operadores Relacionais #
##########################

Operador "=" de atribuição
Operador "==" comparação de valores, valor 
Operador "<" menor que
Operador ">" maior que
Operador "<=" menor ou igual
Operador ">=" maior ou igual
Operador "!=" de verificação se o valor é diferente

"""

#Exemplos
x = 10 # O operador = normalmente usado quando precisamos atribuir um valor a uma variavel, porém tem outros usos veremos mais a frente
y = 5
print(x == y)   # valor de x é igual a y
print(x < y)    # x é menor que y
print(x > y)    # x é maior que y
print(x <= y)   # x é menor ou igual a y
print(x >= y)   # x é maior ou igual a y
print(x != y)   # x é diferente de y

